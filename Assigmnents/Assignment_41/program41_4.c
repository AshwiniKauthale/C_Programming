//
//  Entry Point Function
//
///////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0,iRet = 0;

    printf("Enter number : \n");
    scanf("%d",&iValue);
    iRet = Fact(iValue);

    printf("Factorial is : %d",iRet);

    return 0;
}
