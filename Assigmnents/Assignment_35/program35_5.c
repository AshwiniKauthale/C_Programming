void Reverse(char str[])
{
    char *temp = str;
    while(*str != '\0')
    {
        str++;
    }
    while(str >= temp)
    {
        printf("%c",*str);
        str--;
    }
    
}

///////////////////////////////////////////////////////////////////
//
//  Entry Point Function for Java Application
//
///////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20];

    printf("Enter the string :\n");
    scanf("%[^'\n]s",Arr);

    Reverse(Arr);
    return 0;
}

