void StrCpy(char *src,char *dest)
{
    while(*src != '\0')
    {
        if(*src != ' ')
        {
            *dest = *src;
            dest++;
        }
        src++;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////
//
//  Entry Point Function for Java Application
//
///////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};
    char Brr[50] = {'\0'}; 

    printf("Enter String :\n");
    scanf("%[^'\n']",Arr,Brr);

    StrCpy(Arr,Brr);
    printf("Updated string is :%s",Brr);

    return 0;
}

