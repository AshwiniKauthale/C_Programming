void Display(char ch)
{
    if((ch >= 'A' && ch <= 'Z') )
    {
        printf("%c",(ch + 32));
    }
    else
    if((ch >= 'a' && ch <= 'z') )
    {
        printf("%c",(ch - 32));
    }
    else
    {
        printf("%c",ch);
    }
}

///////////////////////////////////////////////////////////////////
//
//  Entry Point Function for Java Application
//
///////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter the character :\n");
    scanf("%c",&cValue);

    Display(cValue);

}

