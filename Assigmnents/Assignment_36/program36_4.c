void DisplayDigit(char str[])
{
    char *start=str;
    char *dest=str;
    
    while(*start != '\0')
    {
            if(*start >= '0' && *start <= '9')
            {
                *dest=*start;
                dest++;
            }
            start++;
    }
   *dest='\0';
}

