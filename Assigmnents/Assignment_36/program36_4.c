///////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter String : \n");
    scanf("%[^'\n']s",Arr);

    DisplayDigit(Arr);
    printf("Digits are : %s\n",Arr);

    return 0;
}

