#include<stdio.h>

//////////////////////////////////////////////////////////
//
//    Function Name :    Display
//    Input :            Nothing
//    Output :           Display number upto 5
//    Description :      Used to display the number using recursion
//    Author :           Ashwini Vishnu Kauthale
//    Data :             08/02/2026
//
//////////////////////////////////////////////////////////

void Display()
{
    static int iNo = 5;
    if(iNo >= 1)
    {
        printf("%d\t",iNo);
        iNo--;
        Display();
    }
}

//////////////////////////////////////////////////////////
//
//      Entry Point Function
//
//////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}