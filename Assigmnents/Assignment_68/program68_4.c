#include<stdio.h>

//////////////////////////////////////////////////////////
//
//    Function Name :    Display
//    Input :            Nothing
//    Output :           Display Alphabet in reverse order
//    Description :      Used to display the alphabet using recursion
//    Author :           Ashwini Vishnu Kauthale
//    Data :             08/02/2026
//
//////////////////////////////////////////////////////////

void Display()
{
    static char ch = 'A';

    if(ch <= 'F')
    {
        printf("%c\t",ch);
        ch++;
        Display();
    }
}

//////////////////////////////////////////////////////////
//
//      Entry Point Function
//
//////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}